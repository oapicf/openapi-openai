/*
 * CompletionUsage_completion_tokens_details.h
 *
 * Breakdown of tokens used in a completion.
 */

#ifndef _CompletionUsage_completion_tokens_details_H_
#define _CompletionUsage_completion_tokens_details_H_


#include <string>
#include "Object.h"

/** \defgroup Models Data Structures for API
 *  Classes containing all the Data Structures needed for calling/returned by API endpoints
 *
 */

namespace Tizen {
namespace ArtikCloud {


/*! \brief Breakdown of tokens used in a completion.
 *
 *  \ingroup Models
 *
 */

class CompletionUsage_completion_tokens_details : public Object {
public:
	/*! \brief Constructor.
	 */
	CompletionUsage_completion_tokens_details();
	CompletionUsage_completion_tokens_details(char* str);

	/*! \brief Destructor.
	 */
	virtual ~CompletionUsage_completion_tokens_details();

	/*! \brief Retrieve a string JSON representation of this class.
	 */
	char* toJson();

	/*! \brief Fills in members of this class from JSON string representing it.
	 */
	void fromJson(char* jsonStr);

	/*! \brief Get When using Predicted Outputs, the number of tokens in the prediction that appeared in the completion. 
	 */
	int getAcceptedPredictionTokens();

	/*! \brief Set When using Predicted Outputs, the number of tokens in the prediction that appeared in the completion. 
	 */
	void setAcceptedPredictionTokens(int  accepted_prediction_tokens);
	/*! \brief Get Audio input tokens generated by the model.
	 */
	int getAudioTokens();

	/*! \brief Set Audio input tokens generated by the model.
	 */
	void setAudioTokens(int  audio_tokens);
	/*! \brief Get Tokens generated by the model for reasoning.
	 */
	int getReasoningTokens();

	/*! \brief Set Tokens generated by the model for reasoning.
	 */
	void setReasoningTokens(int  reasoning_tokens);
	/*! \brief Get When using Predicted Outputs, the number of tokens in the prediction that did not appear in the completion. However, like reasoning tokens, these tokens are still counted in the total completion tokens for purposes of billing, output, and context window limits. 
	 */
	int getRejectedPredictionTokens();

	/*! \brief Set When using Predicted Outputs, the number of tokens in the prediction that did not appear in the completion. However, like reasoning tokens, these tokens are still counted in the total completion tokens for purposes of billing, output, and context window limits. 
	 */
	void setRejectedPredictionTokens(int  rejected_prediction_tokens);

private:
	int accepted_prediction_tokens;
	int audio_tokens;
	int reasoning_tokens;
	int rejected_prediction_tokens;
	void __init();
	void __cleanup();

};
}
}

#endif /* _CompletionUsage_completion_tokens_details_H_ */
