/*
 * RealtimeConversationItem.h
 *
 * The item to add to the conversation.
 */

#ifndef _RealtimeConversationItem_H_
#define _RealtimeConversationItem_H_


#include <string>
#include "RealtimeConversationItem_content_inner.h"
#include <list>
#include "Object.h"

/** \defgroup Models Data Structures for API
 *  Classes containing all the Data Structures needed for calling/returned by API endpoints
 *
 */

namespace Tizen {
namespace ArtikCloud {


/*! \brief The item to add to the conversation.
 *
 *  \ingroup Models
 *
 */

class RealtimeConversationItem : public Object {
public:
	/*! \brief Constructor.
	 */
	RealtimeConversationItem();
	RealtimeConversationItem(char* str);

	/*! \brief Destructor.
	 */
	virtual ~RealtimeConversationItem();

	/*! \brief Retrieve a string JSON representation of this class.
	 */
	char* toJson();

	/*! \brief Fills in members of this class from JSON string representing it.
	 */
	void fromJson(char* jsonStr);

	/*! \brief Get The unique ID of the item, this can be generated by the client to help  manage server-side context, but is not required because the server will  generate one if not provided. 
	 */
	std::string getId();

	/*! \brief Set The unique ID of the item, this can be generated by the client to help  manage server-side context, but is not required because the server will  generate one if not provided. 
	 */
	void setId(std::string  id);
	/*! \brief Get The type of the item (`message`, `function_call`, `function_call_output`). 
	 */
	std::string getType();

	/*! \brief Set The type of the item (`message`, `function_call`, `function_call_output`). 
	 */
	void setType(std::string  type);
	/*! \brief Get Identifier for the API object being returned - always `realtime.item`. 
	 */
	std::string getObject();

	/*! \brief Set Identifier for the API object being returned - always `realtime.item`. 
	 */
	void setObject(std::string  object);
	/*! \brief Get The status of the item (`completed`, `incomplete`). These have no effect  on the conversation, but are accepted for consistency with the  `conversation.item.created` event. 
	 */
	std::string getStatus();

	/*! \brief Set The status of the item (`completed`, `incomplete`). These have no effect  on the conversation, but are accepted for consistency with the  `conversation.item.created` event. 
	 */
	void setStatus(std::string  status);
	/*! \brief Get The role of the message sender (`user`, `assistant`, `system`), only  applicable for `message` items. 
	 */
	std::string getRole();

	/*! \brief Set The role of the message sender (`user`, `assistant`, `system`), only  applicable for `message` items. 
	 */
	void setRole(std::string  role);
	/*! \brief Get The content of the message, applicable for `message` items.  - Message items of role `system` support only `input_text` content - Message items of role `user` support `input_text` and `input_audio`    content - Message items of role `assistant` support `text` content. 
	 */
	std::list<RealtimeConversationItem_content_inner> getContent();

	/*! \brief Set The content of the message, applicable for `message` items.  - Message items of role `system` support only `input_text` content - Message items of role `user` support `input_text` and `input_audio`    content - Message items of role `assistant` support `text` content. 
	 */
	void setContent(std::list <RealtimeConversationItem_content_inner> content);
	/*! \brief Get The ID of the function call (for `function_call` and  `function_call_output` items). If passed on a `function_call_output`  item, the server will check that a `function_call` item with the same  ID exists in the conversation history. 
	 */
	std::string getCallId();

	/*! \brief Set The ID of the function call (for `function_call` and  `function_call_output` items). If passed on a `function_call_output`  item, the server will check that a `function_call` item with the same  ID exists in the conversation history. 
	 */
	void setCallId(std::string  call_id);
	/*! \brief Get The name of the function being called (for `function_call` items). 
	 */
	std::string getName();

	/*! \brief Set The name of the function being called (for `function_call` items). 
	 */
	void setName(std::string  name);
	/*! \brief Get The arguments of the function call (for `function_call` items). 
	 */
	std::string getArguments();

	/*! \brief Set The arguments of the function call (for `function_call` items). 
	 */
	void setArguments(std::string  arguments);
	/*! \brief Get The output of the function call (for `function_call_output` items). 
	 */
	std::string getOutput();

	/*! \brief Set The output of the function call (for `function_call_output` items). 
	 */
	void setOutput(std::string  output);

private:
	std::string id;
	std::string type;
	std::string object;
	std::string status;
	std::string role;
	std::list <RealtimeConversationItem_content_inner>content;
	std::string call_id;
	std::string name;
	std::string arguments;
	std::string output;
	void __init();
	void __cleanup();

};
}
}

#endif /* _RealtimeConversationItem_H_ */
