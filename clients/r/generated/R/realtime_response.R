          stop(paste("Error! \"", this_object$`status`, "\" cannot be assigned to `status`. Must be \"completed\", \"cancelled\", \"failed\", \"incomplete\".", sep = ""))
        }
        self$`status` <- this_object$`status`
      }
      if (!is.null(this_object$`status_details`)) {
        `status_details_object` <- RealtimeResponseStatusDetails$new()
        `status_details_object`$fromJSON(jsonlite::toJSON(this_object$`status_details`, auto_unbox = TRUE, digits = NA))
        self$`status_details` <- `status_details_object`
      }
      if (!is.null(this_object$`output`)) {
        self$`output` <- ApiClient$new()$deserializeObj(this_object$`output`, "array[RealtimeConversationItem]", loadNamespace("openapi"))
      }
      if (!is.null(this_object$`metadata`)) {
        self$`metadata` <- this_object$`metadata`
      }
      if (!is.null(this_object$`usage`)) {
        `usage_object` <- RealtimeResponseUsage$new()
        `usage_object`$fromJSON(jsonlite::toJSON(this_object$`usage`, auto_unbox = TRUE, digits = NA))
        self$`usage` <- `usage_object`
      }
      self
    },

