      if (!is.null(`object`)) {
        if (!(`object` %in% c("realtime.response"))) {
          stop(paste("Error! \"", `object`, "\" cannot be assigned to `object`. Must be \"realtime.response\".", sep = ""))
        }
        if (!(is.character(`object`) && length(`object`) == 1)) {
          stop(paste("Error! Invalid data for `object`. Must be a string:", `object`))
        }
        self$`object` <- `object`
      }
      if (!is.null(`status`)) {
        if (!(`status` %in% c("completed", "cancelled", "failed", "incomplete"))) {
          stop(paste("Error! \"", `status`, "\" cannot be assigned to `status`. Must be \"completed\", \"cancelled\", \"failed\", \"incomplete\".", sep = ""))
        }
        if (!(is.character(`status`) && length(`status`) == 1)) {
          stop(paste("Error! Invalid data for `status`. Must be a string:", `status`))
        }
        self$`status` <- `status`
      }
      if (!is.null(`status_details`)) {
        stopifnot(R6::is.R6(`status_details`))
        self$`status_details` <- `status_details`
      }
      if (!is.null(`output`)) {
        stopifnot(is.vector(`output`), length(`output`) != 0)
        sapply(`output`, function(x) stopifnot(R6::is.R6(x)))
        self$`output` <- `output`
      }
      if (!is.null(`metadata`)) {
        self$`metadata` <- `metadata`
      }
      if (!is.null(`usage`)) {
        stopifnot(R6::is.R6(`usage`))
        self$`usage` <- `usage`
      }
    },

