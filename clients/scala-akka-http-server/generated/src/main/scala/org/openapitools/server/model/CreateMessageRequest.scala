package org.openapitools.server.model


/**
 * @param role The role of the entity that is creating the message. Allowed values include: - `user`: Indicates the message is sent by an actual user and should be used in most cases to represent user-generated messages. - `assistant`: Indicates the message is generated by the assistant. Use this value to insert messages from the assistant into the conversation.  for example: ''null''
 * @param content  for example: ''null''
 * @param attachments A list of files attached to the message, and the tools they should be added to. for example: ''null''
 * @param metadata Set of 16 key-value pairs that can be attached to an object. This can be useful for storing additional information about the object in a structured format. Keys can be a maximum of 64 characters long and values can be a maximum of 512 characters long.  for example: ''null''
*/
final case class CreateMessageRequest (
  role: String,
  content: CreateMessageRequestContent,
  attachments: Option[Seq[CreateMessageRequestAttachmentsInner]] = None,
  metadata: Option[Any] = None
)

