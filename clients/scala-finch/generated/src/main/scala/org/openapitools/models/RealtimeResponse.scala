package org.openapitools.models

import io.circe._
import io.finch.circe._
import io.circe.generic.semiauto._
import io.circe.java8.time._
import org.openapitools._
import org.openapitools.models.RealtimeConversationItem
import org.openapitools.models.RealtimeResponseStatusDetails
import org.openapitools.models.RealtimeResponseUsage
import scala.collection.immutable.Seq

/**
 * The response resource.
 * @param id The unique ID of the response.
 * @param _object The object type, must be `realtime.response`.
 * @param status The final status of the response (`completed`, `cancelled`, `failed`, or  `incomplete`). 
 * @param statusUnderscoredetails 
 * @param output The list of output items generated by the response.
 * @param metadata Developer-provided string key-value pairs associated with this response. 
 * @param usage 
 */
case class RealtimeResponse(id: Option[String],
                _object: Option[String],
                status: Option[String],
                statusUnderscoredetails: Option[RealtimeResponseStatusDetails],
                output: Option[Seq[RealtimeConversationItem]],
                metadata: Option[Object],
                usage: Option[RealtimeResponseUsage]
                )

object RealtimeResponse {
    /**
     * Creates the codec for converting RealtimeResponse from and to JSON.
     */
    implicit val decoder: Decoder[RealtimeResponse] = deriveDecoder
    implicit val encoder: ObjectEncoder[RealtimeResponse] = deriveEncoder
}
