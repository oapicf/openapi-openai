const utils = require('../utils/utils');

module.exports = {
    fields: (prefix = '', isInput = true, isArrayChild = false) => {
        const {keyPrefix, labelPrefix} = utils.buildKeyAndLabel(prefix, isInput, isArrayChild)
        return [
            {
                key: `${keyPrefix}id`,
                label: `Unique identifier for this audio response. - [${labelPrefix}id]`,
                required: true,
                type: 'string',
            },
            {
                key: `${keyPrefix}expires_at`,
                label: `The Unix timestamp (in seconds) for when this audio response will no longer be accessible on the server for use in multi-turn conversations.  - [${labelPrefix}expires_at]`,
                required: true,
                type: 'integer',
            },
            {
                key: `${keyPrefix}data`,
                label: `Base64 encoded audio bytes generated by the model, in the format specified in the request.  - [${labelPrefix}data]`,
                required: true,
                type: 'string',
            },
            {
                key: `${keyPrefix}transcript`,
                label: `Transcript of the audio generated by the model. - [${labelPrefix}transcript]`,
                required: true,
                type: 'string',
            },
        ]
    },
    mapping: (bundle, prefix = '') => {
        const {keyPrefix} = utils.buildKeyAndLabel(prefix)
        return {
            'id': bundle.inputData?.[`${keyPrefix}id`],
            'expires_at': bundle.inputData?.[`${keyPrefix}expires_at`],
            'data': bundle.inputData?.[`${keyPrefix}data`],
            'transcript': bundle.inputData?.[`${keyPrefix}transcript`],
        }
    },
}
