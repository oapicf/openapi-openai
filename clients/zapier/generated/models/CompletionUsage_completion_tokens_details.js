const utils = require('../utils/utils');

module.exports = {
    fields: (prefix = '', isInput = true, isArrayChild = false) => {
        const {keyPrefix, labelPrefix} = utils.buildKeyAndLabel(prefix, isInput, isArrayChild)
        return [
            {
                key: `${keyPrefix}accepted_prediction_tokens`,
                label: `When using Predicted Outputs, the number of tokens in the prediction that appeared in the completion.  - [${labelPrefix}accepted_prediction_tokens]`,
                type: 'integer',
            },
            {
                key: `${keyPrefix}audio_tokens`,
                label: `Audio input tokens generated by the model. - [${labelPrefix}audio_tokens]`,
                type: 'integer',
            },
            {
                key: `${keyPrefix}reasoning_tokens`,
                label: `Tokens generated by the model for reasoning. - [${labelPrefix}reasoning_tokens]`,
                type: 'integer',
            },
            {
                key: `${keyPrefix}rejected_prediction_tokens`,
                label: `When using Predicted Outputs, the number of tokens in the prediction that did not appear in the completion. However, like reasoning tokens, these tokens are still counted in the total completion tokens for purposes of billing, output, and context window limits.  - [${labelPrefix}rejected_prediction_tokens]`,
                type: 'integer',
            },
        ]
    },
    mapping: (bundle, prefix = '') => {
        const {keyPrefix} = utils.buildKeyAndLabel(prefix)
        return {
            'accepted_prediction_tokens': bundle.inputData?.[`${keyPrefix}accepted_prediction_tokens`],
            'audio_tokens': bundle.inputData?.[`${keyPrefix}audio_tokens`],
            'reasoning_tokens': bundle.inputData?.[`${keyPrefix}reasoning_tokens`],
            'rejected_prediction_tokens': bundle.inputData?.[`${keyPrefix}rejected_prediction_tokens`],
        }
    },
}
